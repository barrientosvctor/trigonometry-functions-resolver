/**
 *
 * @param {number} co
 * @param {number} h
 * @returns
 */
export function sen(co, h) {
  return co / h;
}

/**
 *
 * @param {number} ca
 * @param {number} h
 * @returns
 */
export function cos(ca, h) {
  return ca / h;
}

/**
 *
 * @param {number} h
 * @param {number} ca
 * @returns
 */
export function sec(h, ca) {
  return h / ca;
}

/**
 *
 * @param {number} h
 * @param {number} co
 * @returns
 */
export function csc(h, co) {
  return h / co;
}

/**
 *
 * @param {number} co
 * @param {number} ca
 * @returns
 */
export function tan(co, ca) {
  return co / ca;
}

/**
 *
 * @param {number} ca
 * @param {number} co
 * @returns
 */
export function cot(ca, co) {
  return ca / co;
}
